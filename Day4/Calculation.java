package com.java.day4;

public class Calculation {
    public void add(int num1, int num2){
        int res = num1+num2;
        System.out.println(res);
    }

    public void add(int num1, int num2, int num3){
        int res = num1+num2+num3;
        System.out.println(res);
    }
}
